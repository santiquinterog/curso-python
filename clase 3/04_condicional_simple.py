# Un condicional es una estructura de control que permite evaluar si una expresión es verdadera o falsa, y ejecutar un bloque de código en función de ello. 

llave = True

if llave:
    print("Se ejecuta el bloque")

"""
    No es necesario realizar la siguiente comparación
    if llave == True:
        print("Se ejecuta el bloque")

    No es necesario realizar esta comparación, porque la responsabilidad de un condicional, precisamente es evaluar si una expresión es verdadera o falsa.
"""

# Si tengo la llave, es decir si el valor es verdadero, puedo ingrear y se ejecuta el bloque de código.

edad = 15

if edad >= 18:
    print("Eres mayor de edad")

# Si la edad es mayor o igual a 18, se ejecuta el bloque de código, en caso contrario, no se ejecuta, no pasa nada.